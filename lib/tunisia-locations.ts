// Liste des gouvernorats uniquement (pour compatibilité)
export const gouvernorats = Object.keys(tunisiaLocations).sort()

// Fonction pour obtenir les délégations d'un gouvernorat
export function getDelegations(gouvernorat: string): string[] {
  return tunisiaLocations[gouvernorat] || []
}

// Fonction de recherche de gouvernorat (pour autocomplétion)
export function searchGouvernorats(query: string): string[] {
  if (!query.trim()) return gouvernorats
  const searchLower = query.toLowerCase()
  return gouvernorats.filter(g => g.toLowerCase().includes(searchLower))
}

// Fonction de recherche de délégation (pour autocomplétion)
export function searchDelegations(gouvernorat: string, query: string): string[] {
  const delegations = getDelegations(gouvernorat)
  if (!query.trim()) return delegations
  const searchLower = query.toLowerCase()
  return delegations.filter(d => d.toLowerCase().includes(searchLower))
}
